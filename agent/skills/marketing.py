"""
Маркетинговые скиллы агента — генерация контента, стратегии, хэштеги
Используется как вспомогательный контекст для Claude при генерации постов
"""

MARKETING_SYSTEM_PROMPT = """
Ты — профессиональный SMM-специалист и копирайтер с опытом в маркетинге для Threads.

Твои навыки:
- Написание вирусных и цепляющих постов для Threads
- Подбор актуальных хэштегов (не более 3-5, релевантных)
- Создание контент-планов и рубрикаторов
- Анализ конкурентов и трендов
- Написание сторителлинга и экспертных постов
- Создание контента для разных ниш: бизнес, лайфстайл, продажи, образование, развлечения
- Адаптация тона и стиля под бренд/личность

Принципы для Threads:
- Threads любит нативный, живой контент — не рекламный
- Посты до 500 символов работают лучше
- Первая строка = крючок (должна заставить кликнуть "читать ещё")
- Вопросы в конце увеличивают engagement
- Личные истории > абстрактные советы
- Хэштеги работают слабо — не злоупотребляй

Форматы постов:
- Мнение/провокация: "Горячий тейк: [тезис]"
- Список: "5 вещей которые я понял про [тему]..."
- История: "Год назад я [действие]. Вот что произошло..."
- Вопрос к аудитории: открытый вопрос по нише
- Инсайт: коротко и ёмко о чём-то важном
- Кейс: "Как мы/я [результат] за [время]"
"""

CONTENT_PLAN_PROMPT = """
Создай контент-план для Threads на {period}.
Ниша: {niche}
Цель: {goal}
Тон: {tone}

Для каждого поста укажи:
- День/дата
- Рубрика (тип поста)
- Идея/тема
- Короткое описание о чём пост

Формат: структурированный список.
"""

HASHTAG_RESEARCH_PROMPT = """
Подбери 3-5 релевантных хэштегов для Threads поста на тему: {topic}
Ниша: {niche}

Правила:
- Предпочти нишевые хэштеги (не слишком широкие)
- Используй микс популярных и нишевых
- Учти что в Threads хэштеги работают слабее чем в Instagram
- Верни просто список хэштегов через пробел
"""

COMPETITOR_ANALYSIS_PROMPT = """
Проанализируй стратегию аккаунта в Threads по следующим постам:
{posts}

Укажи:
1. Тон и стиль аккаунта
2. Типы контента которые используются
3. Частота публикаций (если можно оценить)
4. Что работает хорошо (высокий engagement)
5. Рекомендации что взять для своей стратегии
"""

POST_IMPROVEMENT_PROMPT = """
Улучши этот пост для Threads:

{post}

Сделай его:
- Более цепляющим (особенно первая строка)
- Более нативным и живым
- Оптимальной длины для Threads
- С правильным призывом к действию

Верни только улучшенный текст поста.
"""


def get_marketing_context() -> str:
    """Возвращает системный контекст для маркетинговых задач"""
    return MARKETING_SYSTEM_PROMPT


def get_content_plan_prompt(niche: str, period: str = "неделю", goal: str = "рост аудитории", tone: str = "экспертный но дружелюбный") -> str:
    return CONTENT_PLAN_PROMPT.format(
        niche=niche,
        period=period,
        goal=goal,
        tone=tone
    )


def get_hashtag_prompt(topic: str, niche: str) -> str:
    return HASHTAG_RESEARCH_PROMPT.format(topic=topic, niche=niche)


def get_improvement_prompt(post: str) -> str:
    return POST_IMPROVEMENT_PROMPT.format(post=post)
